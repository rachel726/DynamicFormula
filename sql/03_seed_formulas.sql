USE DynamicFormula;
GO

DELETE FROM dbo.t_targil;
DBCC CHECKIDENT ('dbo.t_targil', RESEED, 0) WITH NO_INFOMSGS;
GO

-- ── פורמט נוסחאות ──────────────────────────────────────────────────
-- משתנים:  a, b, c, d         (באותיות קטנות)
-- פעולות:  + - * / ( )
-- פונקציות: sqrt(x)  log(x)  abs(x)  power(x,y)   או   x^y
-- ──────────────────────────────────────────────────────────────────

INSERT INTO dbo.t_targil (targil, tnai, targil_false, description)
VALUES
    -- ┌─────────────────────────────────────┐
    -- │  נוסחאות פשוטות                     │
    -- └─────────────────────────────────────┘
    ('a + b',         NULL, NULL, N'חיבור שני שדות'),
    ('c * 2',         NULL, NULL, N'כפל שדה בקבוע'),
    ('b - a',         NULL, NULL, N'חיסור שני שדות'),
    ('d / 4',         NULL, NULL, N'חילוק שדה בקבוע'),
    ('a * b + c',     NULL, NULL, N'צירוף חיבור וכפל'),

    -- ┌─────────────────────────────────────┐
    -- │  נוסחאות מורכבות                    │
    -- └─────────────────────────────────────┘
    ('(a + b) * 8',                            NULL, NULL, N'חיבור כפול קבוע'),
    ('sqrt(c^2 + d^2)',                        NULL, NULL, N'שורש סכום ריבועים (פיתגורס)'),
    ('log(b) + c',                             NULL, NULL, N'לוגריתם טבעי ועוד שדה'),
    ('abs(d - b)',                             NULL, NULL, N'ערך מוחלט של הפרש'),
    ('sqrt(a^2 + b^2 + c^2 + d^2)',           NULL, NULL, N'מרחק אוקלידי 4D'),
    ('(a + b) * (c - d) / 2',                  NULL, NULL, N'שילוב 4 משתנים'),

    -- ┌─────────────────────────────────────┐
    -- │  נוסחאות עם תנאים (בונוס!)          │
    -- └─────────────────────────────────────┘
    ('b * 2',         'a > 5',   'b / 2',       N'תנאי: אם a>5 אזי b*2 אחרת b/2'),
    ('a + 1',         'b < 10',  'd - 1',       N'תנאי: אם b<10 אזי a+1 אחרת d-1'),
    ('1',             'a = c',   '0',           N'תנאי שוויון: 1 אם a==c אחרת 0'),
    ('a * c',         'a > b',   'b * d',       N'מכפלה מותנית'),
    ('(a + b + c + d) / 4',   'd >= 50',  'a + b + c + d',   N'ממוצע או סכום לפי d');
GO

-- ── אימות + תצוגה ─────────────────────────────────────────────────
PRINT '═══════════════════════════════════════════════════';
PRINT '  Formulas inserted: ' + CAST(@@ROWCOUNT AS VARCHAR);
PRINT '═══════════════════════════════════════════════════';

SELECT
    targil_id   AS id,
    targil      AS [נוסחה],
    tnai        AS [תנאי],
    targil_false AS [אם לא מתקיים],
    description AS [תיאור]
FROM dbo.t_targil
ORDER BY targil_id;
GO
